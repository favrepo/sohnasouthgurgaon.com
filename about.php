				<p>Gurgaon has turned into a glorious investment magnet that hasn't looked back since the millennium. The Real Estate market has been aggressively broadening its scope in Gurgaon and now the extensions of the region have started to take over the investment part. The hinterland of Sohna has now joined the ranks of similar extensions of Delhi and Noida. A group of prominent developers has already started to work on South of Gurgaon's development plan. A total of 6000 crores have been put into this location that promises number of townships and malls. A 15-minute drive to Golf Course Road, South Gurgaon houses CREDAI's members. With a proposed extension to Huda City Centre metro station, South Gurgaon has an assured connectivity and a thriving social culture by 2031. </p>

<p>The master plan of South Gurgaon promises a better and affordable lifestyle as compared to the high-priced residential townships of the main Gurgaon. Developers like Supertech, Ireo, Ashiana Mulberry, Central Park, Eldeco and International Land Developers have already set up their project launches in the region and the availability of Badshahpur Flyover makes South Gurgaon a worthy investment opportunity. The diminishing spaces of Central Gurgaon required the extension that South Gurgaon is providing and the association with CREDAI makes it an all the more impressive investment opportunity. A place that combines a good lifestyle, commutable roads and development makes for a perfect living option.<p>

<p>Most of the projects have been structured under an affordable view that propels a middle-class family to own a property sooner. The aim of South Gurgaon's residential model is to provide homes that are affordable and half the price of any apartment that there is, in main Gurgaon. Making South Gurgaon a preferable living option in NCR is already underway with Ireo Nuspark and Gold Souk's Golf Links in the making. Developers have put their money and resources into South Gurgaon to make housing a feasible option for Gurgaonites. A place where prices can be negotiated to one's budget and where people can see development rocketing high in the coming years-this is South Gurgaon for you.</p>

<p>The speck of commercialization touched Sohna very early and now after nearly half a decade's worth of investment and construction, South Gurgaon has commercial and office spaces brimming with glory. With global retail giants like Shoppers' stop, Big Bazaar and Hotel Park inn in hospitality sector, South Gurgaon is filling up with such names. The agenda is to attract investment and provide the best residential opportunity in South Gurgaon, thus making it the next big realty picture in the radius of Delhi-NCR</p>

